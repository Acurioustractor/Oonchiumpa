async function updateLawStudentsPost() {
  console.log('📝 Updating law students blog post...\n');

  const { data: posts, error: findError } = await supabase
    .from('blog_posts')
    .select('id, title')
    .ilike('title', '%Learning Law%');

  if (findError || !posts || posts.length === 0) {
    console.error('Error finding post:', findError);
    return;
  }

  const post = posts[0];
  console.log(`Updating: "${post.title}"\n`);

  const { error: updateError } = await supabase
    .from('blog_posts')
    .update({
      content: newContent,
      updated_at: new Date().toISOString()
    })
    .eq('id', post.id);

  if (updateError) {
    console.error('Error updating:', updateError);
  } else {
    console.log('✅ Successfully updated law students blog post!');
    console.log('   💬 Added authentic quotes from 5 students');
    console.log('   🎥 Added 3 video link placeholders');
    console.log('   📸 Added 8 photo placeholders\n');
    console.log('NEXT STEPS:');
    console.log('1. Replace YOUR_CHELSEA_VIDEO_LINK_HERE with Chelsea\'s video URL');
    console.log('2. Replace YOUR_SUZIE_VIDEO_LINK_HERE with Suzie\'s video URL');
    console.log('3. Replace YOUR_ADELAIDE_VIDEO_LINK_HERE with Adelaide\'s video URL');
    console.log('4. Tag photos in your gallery with keywords like "law-students-2024"');
    console.log('5. Replace #LAW_STUDENT_PHOTO_X placeholders with actual photo URLs');
  }
}

